@ExtendWith(MockitoExtension.class)
@SpringBootTest
public class ObtenerDiplomaControllerTests {

    @Mock
    IObtenerDiplomaService service;

    @InjectMocks
    ObtenerDiplomaController controller;

    private StudentDTO stu;

    @BeforeEach
    public void setUp() {
        stu = TestUtilsGenerator.getStudentWith3Subjects("Marco");
        TestUtilsGenerator.appendNewStudent(stu);
    }
    @BeforeAll
    static void setupUP(){
        TestUtilsGenerator.emptyUsersFile();
    }

    @Test
    public void obtenerDiploma() {
        when(service.analyzeScores(9999L)).thenReturn(stu);

        StudentDTO result = controller.analyzeScores(9999L);
        verify(service, atLeastOnce()).analyzeScores(9999L);
        assertEquals(stu, result);
    }

}
