package POO;

public class Persona {
    String nombre;
    int edad;
    String dni;
    double altura;
    double peso;

    public Persona(String nombre, int edad, String dni, double altura, double peso) {
        this.nombre = nombre;
        this.edad = edad;
        this.dni = dni;
        this.altura = altura;
        this.peso = peso;
    }
}
