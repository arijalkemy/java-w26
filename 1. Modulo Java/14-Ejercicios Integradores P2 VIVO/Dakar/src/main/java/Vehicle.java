public class Vehicle {
    private int speed;
    private int acceleration;
    private int angleOfTurn;
    private String patent;
    private int weight;
    private int wheels;

    public Vehicle(int speed, int acceleration, int angleOfTurn, String patent, int weight, int wheels) {
        this.speed = speed;
        this.acceleration = acceleration;
        this.angleOfTurn = angleOfTurn;
        this.patent = patent;
        this.weight = weight;
        this.wheels = wheels;
    }
}
