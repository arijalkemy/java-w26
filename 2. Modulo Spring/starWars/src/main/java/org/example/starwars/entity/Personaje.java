@AllArgsConstructor
@NoArgsConstructor
public class Personaje {
    @JsonProperty()
    private String name;
    private int height;
    private int mass;
    private String hairColor;
    private String skinColor;
    private String eyeColor;
    private String birthYear;
    private String gender;
    private String homeworld;
    private String species;
}
